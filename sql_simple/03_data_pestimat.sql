SELECT
    'PESTIMAT Matrix' AS table_name,
    COUNT(*) AS nb_lignes,
    COUNT(DISTINCT matiere_active) AS nb_pesticides,
    COUNT(DISTINCT culture) AS nb_cultures,
    MIN(periode_debut) AS annee_min,
    MAX(periode_fin) AS annee_max
FROM PESTIMAT_Matrix;

